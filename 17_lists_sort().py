phones_1 = [ 'apple', "samsung", 'moto', 'spice', 'lenovo' ]
print(phones_1)

phones_1.sort()                              # this method sorts lists into alphabetical order
print(phones_1)

phones_2 = [ 'apple', "samsung", 'moto', 'spice', 'lenovo' ]
print(phones_2)

phones_2.sort( reverse = True )              # this method sorts lists into reverse alphabetical order with reverse=True arguement
print(phones_2)
